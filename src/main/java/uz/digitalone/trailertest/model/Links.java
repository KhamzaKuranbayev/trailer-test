package uz.digitalone.trailertest.model;

public class Links {
    private String self;

    private String related;

    public void setSelf(String self) {
        this.self = self;
    }

    public String getSelf() {
        return this.self;
    }

    public void setRelated(String related) {
        this.related = related;
    }

    public String getRelated() {
        return this.related;
    }
}
